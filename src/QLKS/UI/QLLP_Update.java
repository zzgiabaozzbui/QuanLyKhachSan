    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        
        try {
        String LP=txtLP.getText();
        Float GiaP=Float.parseFloat(txtGP.getText());
        String MoTa=txtMoTa.getText();
        LoaiPhong lp=new LoaiPhong();
            if( !MoTa.trim().equals("") && MoTa!=null)
            {
            lp.setLoaiPhong(LP);
            lp.setGiaPhong(GiaP);
            lp.setMoTaPhong(MoTa);
            lpd.update(lp);
            }
            else
            {
            JOptionPane.showMessageDialog(null,"Phải nhập điền đầy đủ thông tin !!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Phải nhập đúng kiểu dữ liệu giá phòng !");
        }

        
        
    }//GEN-LAST:event_btnSaveActionPerformed

