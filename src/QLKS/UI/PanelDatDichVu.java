    private void addCbo() {
        KhachHang kh;
        DefaultComboBoxModel model1=new DefaultComboBoxModel();
        for (int i = 0; i < ddvd.getListLKH().size(); i++) {
             kh=ddvd.getListLKH().get(i);
             model1.addElement(kh);
        }
        cboSDT.setModel(model1);
        DichVu dv;
        DefaultComboBoxModel model2=new DefaultComboBoxModel();
        for (int i = 0; i < dvd.getList().size(); i++) {
             dv=dvd.getList().get(i);
             model2.addElement(dv);
        }
       cboTenDV.setModel(model2);
    }
