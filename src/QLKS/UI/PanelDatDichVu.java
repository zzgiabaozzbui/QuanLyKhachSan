    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
               KhachHang kh=(KhachHang) cboSDT.getSelectedItem();
        DichVu dv=(DichVu) cboTenDV.getSelectedItem();
        
        try {
             String MaHDDV=txtMaHDDV.getText();
             String GhiChu=txtGhiChu.getText();
             int SoLuong=(int) spinSL.getValue();
             float DonGia=dv.getPhiDV();
             Date time=(Date) spinTime.getValue();
             Timestamp times=new Timestamp(time.getTime());
             String MaKh=kh.getMaKH();
             String MaDV=dv.getMaDV();
            
            if(!MaHDDV.trim().equals("") && MaHDDV!=null)
            {
             DatDichVu ddv=new DatDichVu();
             
             ddv.setMaHDDV(MaHDDV);
             ddv.setMaKH(MaKh);
             ddv.setMaDV(MaDV);
             ddv.setGhiChu(GhiChu);
             ddv.setSoLuong(SoLuong);
             ddv.setMaNV(new PropertiesNVNow().getRemember().get(0));
             ddv.setThoiGianBatDauSDDV(times);
             ddv.setThoiGianDat(timestamp);
             ddv.setThanhTien(DonGia*SoLuong);
             
                if(ddvd.findT(MaHDDV)==0)
                {
                 ddvd.insert(ddv);
                }
                else
                {
                 JOptionPane.showMessageDialog(null,"Mã hóa đơn dịch vụ tồn tại !!");
                }
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Phải điền đầy đủ thông tin !!");
            }
             
             
             
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Nhập đúng định dạng  !!");
        }
    }//GEN-LAST:event_btnAddActionPerformed


