    public void addTable()
    {
        Vector row = null;
        DefaultTableModel model=new DefaultTableModel();
        model.addColumn("Mã nhập kho");
        model.addColumn("Tên nhà cung cấp");
        model.addColumn("Tên nhân viên");
        model.addColumn("Ngày nhập");
        model.addColumn("Chiết khấu");
        model.addColumn("Thành tiền");
        NhapKho nk;
        for (int i = 0; i < nkd.getList().size(); i++) {
            nk=nkd.getList().get(i);
            row=new Vector();
            row.add(nk.getMaNhapKho());
            row.add(nkd.getData(nk.getMaNCC(),"MaNCC"));
            row.add(nkd.getData(nk.getMaNV(), "MaNV"));
            row.add(formatter.format(nk.getNgayNhap()));
            row.add(nk.getChietKhau());
            row.add(nk.getThanhTien());
            model.addRow(row);
        }
        table.setModel(model);
        
    }
    
